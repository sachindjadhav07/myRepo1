package string;

public class Reverse_String_By_Using_StringBuffer_Class {

	public static void main(String[] args)
	{
		
		StringBuffer sb1= new StringBuffer("Velocity");
		
		StringBuffer ob1= sb1.reverse();

	    System.out.println("Reverse String is "+ob1);
	    
	    
	    System.out.println("==================================");
	    
	    
	    StringBuffer sb2= new StringBuffer("Sachin");
	    
	    StringBuffer ob2=sb2.reverse();
	    
	    System.out.println("Reverse String is "+ob2);
	    
	    
	    System.out.println("==================================");
	    
	    
	    StringBuffer sb3= new StringBuffer("Jadhav");
	    
	    StringBuffer ob3=sb3.reverse();
	    
	    System.out.println("Reverse String is "+ob3);
	    
		
	}

}
