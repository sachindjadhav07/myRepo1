package control_statements;

public class If_else_study
{

	public static void main(String[] args)
	{
    // if my marks are greater than or equals to 40, then I will be pass else I will be fail
		
		int marks=40;
		
		if (marks>=40)
		{
			System.out.println("I am pass");
		}
		
		else
		{
			System.out.println("I am fail");
		}
		

	}

}
