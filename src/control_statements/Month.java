package control_statements;

public class Month {

	public static void main(String[] args) 
	{
		
		String month="Dec";
		
		switch (month)
		{
		case "Jan": System.out.println("This is 1st month of year");
		break;
			
		case "Feb": System.out.println("This is 2nd month of year");
		break;
		    
		case "Mar": System.out.println("This is 3rd month of year");
		break;
		
		case "Apr": System.out.println("This is 4th month of year");
        break;
        
		case "May": System.out.println("This is 5th month of year");
		break;
		
		case "June": System.out.println("This is 6th month of year");
		break;
	
		case "July": System.out.println("This is 7th month of year");
		break;
		
		case "Aug": System.out.println("This is 8th month of year");
		break;
		
		case "Sept": System.out.println("This is 9th month of year");
		break;
		
		case "Oct": System.out.println("This is 10th month of year");
		break;
		
		case "Nov": System.out.println("This is 11th month of year");
		break;
		
		case "Dec": System.out.println("This is 12th month of year");
		break;
		
		default: System.out.println(" Your Wrong Choise");
			break;
		}
		

	}

}
