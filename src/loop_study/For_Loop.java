package loop_study;

public class For_Loop {

	public static void main(String[] args) 
	{
		for(int i=1; i<=5; i++)
		{
			System.out.println("This is for loop for increament");
			System.out.println(i);
		}
		
		
		System.out.println("==================================");
		
		
		for(int i=5; i>=1; i--)
		{
			System.out.println("This is for loop for decreament");
			System.out.println(i);
		}
		
		System.out.println("==================================");
		
		for(int i=10; i>=0; i--)
		{
			System.out.println("Decrement from 10 to 0");
			System.out.println(i);
		}
		
		

	}

}
