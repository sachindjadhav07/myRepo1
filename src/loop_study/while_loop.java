package loop_study;

public class while_loop {

	public static void main(String[] args)
	{
		
		int i=1;
		
		while(i<=5)
		{
			System.out.println("Velocity"+i);
			i++;
		}
		
		int j=3;
		
		while(j>=-2)
		{
			System.out.println("Pune"+j);
			j--;
		}
		
		
		int k=10;
		
		while(k>=0)
		{
			System.out.println("Sachin"+k);
			k--;
		}
			
		
		
		

	}

}
